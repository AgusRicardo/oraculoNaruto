let verificar = document.getElementById('error');
let btnPlayGame = document.getElementById('btn-play-game').addEventListener('click', function (e) {
    loading.style.display = 'flex';
    page1.style.display = 'none';
    e.preventDefault()
    setTimeout(function (){
        inputPlayer1 = document.getElementById('input-player1').value
        inputPlayer2 = document.getElementById('input-player2').value
        document.getElementById('input-player1').value = '';
        document.getElementById('input-player2').value = '';
        if (inputPlayer1 != '' && inputPlayer2 != '') {
            loading.style.display = 'none';
            page1.style.display = 'none';
            page2.style.display = 'block';
        }else{
            loading.style.display = 'none';
            page1.style.display = 'block';
            let nodoError = document.createElement('h5')
            let txtNodoError = document.createTextNode('Ingrese ambos nombres por favor')
            nodoError.appendChild(txtNodoError)
            verificar.appendChild(nodoError)
        }
    }, 3000)
});

